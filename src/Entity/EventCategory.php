<?php

namespace FOSOpenScouting\Keeo\Entity;

class EventCategory extends Entity
{
    protected $id;
    protected $name;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }
}